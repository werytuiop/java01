package org.java.condition;

public class If01 {

	public static void main(String[] args) {

		System.out.println("<if문>");

		int num = 10;

		if (num >= 10) {
			System.out.println("10보다 크거나 같음");
		}

		if (num >= 10) System.out.println("10보다 크거나 같음");

		if (num >= 10)
			System.out.println("10보다 크거나 같음");

	}

}
